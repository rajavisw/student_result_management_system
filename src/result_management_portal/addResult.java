    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String maths=jTextField2.getText();
        String physics=jTextField3.getText();
        String dbms=jTextField4.getText();
        String os=jTextField5.getText();
        String cn=jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Raja@1616");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,maths,physics,dbms,os,cn) values('"+rollno+"','"+maths+"','"+physics+"','"+dbms+"','"+os+"','"+cn+"')");
                JOptionPane.showMessageDialog(null,"Marks saved successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"this roll number is not registered.");
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

